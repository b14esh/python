# Имена аргумента можем задовать такие как мы захотим
def multiplay(number):
    print(number * 2 )

multiplay(2)
multiplay(5)

# Аргументами называются только те значения каторые мы передаем при вызови функции
# т.е. multiplay(number) тут number - параметр
# multiplay(2) тут 2 - аргумент
# и для закрепления  multiplay(5) тут 5 - аргумент


