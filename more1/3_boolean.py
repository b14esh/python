#print(10 == 11)
print(f"Boolean пример:\n 10 равно 11, (10 == 11)  \n Ответ: {10 == 11}")
print(f"\nBoolean пример:\n 10 не равно 11, (10 != 11) \n Ответ: {10 != 11}")
print(f"\nBoolean пример:\n 5 больще 10, (5 > 10)\n Ответ: {5 > 10}")
